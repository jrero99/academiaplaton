const dictionaries: Record<Language, Dict> = { ca, es };

// Resuelve una clave en el idioma activo. Si falta, cae al idioma por defecto.
// Si tampoco existe ahí, devuelve la propia clave para que falle visible y
// sea fácil de detectar en revisión visual.
export function translate(
  key: string,
  lang: Language,
  params?: Record<string, string | number>,
): string {
  let str =
    dictionaries[lang][key] ??
    dictionaries[DEFAULT_LANGUAGE][key] ??
    key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.split(`{${k}}`).join(String(v));
    }
  }
  return str;
}

